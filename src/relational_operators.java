import java.util.Scanner;

public class relational_operators {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("enter the no. ");
        int a=sc.nextInt();
        int b=sc.nextInt();
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a!=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a>=b);
    }
}
